package org.springframework.javapoet.test;

/**
 * A dynamically generated Java file.
 *
 * @author Phillip Webb
 */
class DynamicallyGeneratedJavaFile extends DynamicallyGeneratedFile {

	private final GeneratedClassName className;

	DynamicallyGeneratedJavaFile(GeneratedClassName className) {
		this.className = className;
	}

	GeneratedClassName getClassName() {
		return this.className;
	}

}
